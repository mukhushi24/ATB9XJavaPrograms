package Task.Task_11102024;

public class Lab_011bitwise_operator {
    public static void main (String []args){
        int d = 0b1010;
        int e = 0b1100;
        System.out.println(d&e);
        System.out.println(d|e);
        System.out.println(d^e);
        System.out.println(~d);
        System.out.println(d<<2);
        System.out.println(e>>1);

    }
}
