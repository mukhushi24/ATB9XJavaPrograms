package Task.Task_11102024;

public class Lab_012shift_operator {
    public static void main (String[]args){
        int a=10;
        //left shift operator
        System.out.println(a<<1);
        //signed right shift operator
        System.out.println(a>>1);
       // unsigned right shift operator
        System.out.println(a>>>1);

    }
}
