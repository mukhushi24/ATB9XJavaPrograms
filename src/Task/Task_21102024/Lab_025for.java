package Task.Task_21102024;

public class Lab_025for {
    public static void main(String[] args){
        for(int i=0;i>0;i++){
            System.out.println(i);
        }
    }
}
//The loop condition i > 0 is false from the start because i is initialized to 0.
// Therefore, the loop body (the System.out.println(i) line) will never be executed,
// and nothing will be printed to the console.